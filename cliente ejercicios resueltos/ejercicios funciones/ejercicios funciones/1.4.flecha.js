// Definir función flecha que verifica si un número es par
const esPar = (numero) => numero % 2 === 0;

// Probar la función con varios números
console.log(esPar(4)); // Resultado esperado: true
console.log(esPar(7)); // Resultado esperado: false
console.log(esPar(0)); // Resultado esperado: true
console.log(esPar(15)); // Resultado esperado: false
